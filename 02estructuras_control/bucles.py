print("\nFor dell 1 al 5")

for i in range(1, 6): #Entra en el bucle "for" hasta que i sea 6 (empezando desde 1)
    print(i) #Imprime los números del 1 al 5, cuando la i es 6 no lo escribe ya que en el momento de la igualdad se saldrá del for

#-----------------------------------------------------------------------------------------------------------------------------------0

print("\nWhile del 1 al 6")

contador = 1 #Creo un contador desde el número 1

while contador <= 6: #entra en el bucle "while" siempre que la variable "contador" tenga un valor de 6 o inferior
    print(contador) #Muestra por pantalla el valor de la variable "contador"
    contador += 1 #La variable contador aumenta en 1 tras mostrarse por pantalla para hacer finito el bucle
